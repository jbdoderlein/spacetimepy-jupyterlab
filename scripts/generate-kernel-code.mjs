import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const outputPath = resolve(
  projectRoot,
  'src/generated/kernel-sources.ts'
);
const sources = [
  ['WORKFLOW_SUMMARY_SOURCE', 'src/kernel/workflow-summary.py'],
  ['TRACE_QUERY_TEMPLATE', 'src/kernel/trace-query.py'],
  ['REEXECUTE_VARIANT_TEMPLATE', 'src/kernel/reexecute-variant.py']
];

const declarations = await Promise.all(
  sources.map(async ([exportName, relativePath]) => {
    const source = await readFile(resolve(projectRoot, relativePath), 'utf8');
    const embeddedSource = `\n${source}`;
    return `export const ${exportName} = ${JSON.stringify(embeddedSource)};`;
  })
);
const output = [
  '// Generated by scripts/generate-kernel-code.mjs. Do not edit.',
  '',
  ...declarations,
  ''
].join('\n');

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, output, 'utf8');
